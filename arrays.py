number = []
number.extend([1, 2, 3, 4, 5, 6, 7])  # add new list to existing list
print(number)
number.append(1)  # add the value end of the list
print(number)
number.insert(0, 2) # insert any value to particular index
print(number)
number.remove(3)  # remove the specified value in parenthesis
print(number)